def get_district_geocode(district: str, state: str) -> tuple[float, float] | None:
    """
    Get geocode for a district from the pre-populated database.

    Args:
        district: District name
        state: State name

    Returns:
        Tuple of (latitude, longitude) or None if not found
    """
    key = f"{district}|{state}"
    return DISTRICT_GEOCODES.get(key)


def get_all_states() -> list[str]:
    """Get list of all states in the database."""
    return sorted(set(k.split("|")[1] for k in DISTRICT_GEOCODES.keys()))


def get_districts_for_state(state: str) -> list[str]:
    """Get list of all districts for a state."""
    return sorted([
        k.split("|")[0]
        for k in DISTRICT_GEOCODES.keys()
        if k.split("|")[1] == state
    ])
